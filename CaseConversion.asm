TITLE Case Conversion Program
.MODEL SMALL
.STACK 100H
.DATA
    CR EQU 0DH
    LF EQU 0AH
    IN_MSG DB 'Enter a string : ',CR,LF,'$'
    OUT_MSG DB 'After Case Conversion : ',CR,LF,'$'
    STR1 DB 100 DUP(?)

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    LEA DX,IN_MSG
    MOV AH,9
    INT 21H

    MOV SI,0000H
    MOV BX,0000H
    
    
INPUT:
	MOV AH,1
    INT 21H
    CMP AL,CR
    JE END_INPUT
    MOV STR1[SI],AL
    INC SI
    JMP INPUT

END_INPUT:
	MOV AH,2
	MOV DL,CR
	INT 21H
	MOV DL,LF
	INT 21H

	LEA DX,OUT_MSG
	MOV AH,9
	INT 21H

	CMP SI,0
	JE EXIT
	JMP PROCESS

PROCESS:
	MOV AH,2
	MOV DL,STR1[BX]
	CMP DL,'A'
	JL DISPLAY_OUTPUT
	CMP DL,'Z'
	JLE TO_LOWER
	CMP DL,'a'
	JL DISPLAY_OUTPUT
	CMP DL,'z'
	JG DISPLAY_OUTPUT
	JMP TO_UPPER


TO_LOWER:
	ADD DL,20H
	JMP DISPLAY_OUTPUT

TO_UPPER:
	SUB DL, 20H
	JMP DISPLAY_OUTPUT

DISPLAY_OUTPUT:
	INT 21H
	INC BX
	CMP BX,SI
	JE EXIT
	JMP PROCESS

EXIT:
    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
    