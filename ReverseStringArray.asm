TITLE Reverse String Using Array
.MODEL SMALL
.STACK 100H
.DATA
    CR EQU 0DH
    LF EQU 0AH
    IN_MSG DB 'Enter a string : ',CR,LF,'$'
    OUT_MSG DB 'After Rerversing : ',CR,LF,'$'
    STR1 DB 100 DUP(?)

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    LEA DX,IN_MSG
    MOV AH,9
    INT 21H

    MOV SI,0000H
    MOV BX,0000H

INPUT:
	MOV AH,1
    INT 21H
    CMP AL,CR
    JE END_INPUT
    MOV STR1[SI],AL
    INC SI
    JMP INPUT

END_INPUT:
	MOV AH,2
	MOV DL,CR
	INT 21H
	MOV DL,LF
	INT 21H

	LEA DX,OUT_MSG
	MOV AH,9
	INT 21H

	CMP SI,0
	JE EXIT
	JMP PROCESS
PROCESS:
	DEC SI
	CMP SI,BX
	JL EXIT
	MOV AH,2
	MOV DL,STR1[SI]
	INT 21H
	JMP PROCESS

EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN